public class ConditionalFun {
    public static void main(String[] args) 
    {
        String name = "Mickey Mouse";

        if (name.length() < 10) 
        {
            System.out.println("Welcome to ConditionalFun!");
            System.out.println(name + " is a short name!");
        }
        
        System.out.println("It doesn't matter how longer or short your name is...");

    }

}
